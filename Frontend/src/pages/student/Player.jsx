import React, { useContext, useEffect, useState } from "react";
import { AppContext } from "../../context/AppContext";
import { useParams } from "react-router-dom";
import assets from "../../assets/assets";
import humanizeDuration from "humanize-duration";
import YouTube from "react-youtube";
import Footer from "../../components/student/Footer";
import Rating from "../../components/student/Rating";

function Player() {
  const { EnrolledCourses, calculateChapterTime } = useContext(AppContext);
  const { courseId } = useParams();
  const [courseData, setCourseData] = useState(null);
  const [openSection, setOpenSection] = useState({});
  const [PalyerData, setPalyerData] = useState(null);

  const getCourseData = () => {
    EnrolledCourses.map((course) => {
      if (course._id === courseId) {
        setCourseData(course);
      }
    });
  };

  const toogleSection = (index) => {
    setOpenSection((prev) => ({ ...prev, [index]: !prev[index] }));
  };

  useEffect(() => {
    getCourseData();
  }, [EnrolledCourses]);

  return (
    <>
      <div className="p-4 sm:p-8 flex bg-slate-800 flex-col-reverse md:grid md:grid-cols-2 gap-10 md:px-32">
        {/* Left column  */}
        <div className="text-gray-300">
          <h2 className="text-xl font-semibold">Course Structure</h2>

          <div className="pt-5">
            {courseData &&
              courseData.courseContent.map((chapter, index) => (
                <div
                  key={index}
                  className="border bordere-gray-300 bg-slate-700 mb-2 rounded"
                >
                  <div
                    className="flex items-center justify-between px-4 py-3 
                          cursor-pointer select-none "
                    onClick={() => toogleSection(index)}
                  >
                    <div className="flex items-center gap-2">
                      <img
                        className={`tranform transition-transform ${
                          openSection[index] ? "rotate-180" : ""
                        }`}
                        src={assets.down_arrow_icon}
                        alt="arrow-icon"
                      />
                      <p className="font-medium md:text-base text-sm text-gray-300">
                        {chapter.chapterTitle}
                      </p>
                    </div>
                    <p className="text-sm md:text-default text-gray-300">
                      {chapter.chapterContent.length} lectures -{" "}
                      {calculateChapterTime(chapter)}
                    </p>
                  </div>
                  <div
                    className={`overflow-hidden transition-all duration-300 ${
                      openSection[index] ? "max-h-96" : "max-h-0"
                    }`}
                  >
                    <ul
                      className="list-disc md:pl-10 pl-4 pr-4 py-6 text-gray-300
                            border-t border-gray-500"
                    >
                      {chapter.chapterContent.map((lecture, i) => (
                        <li key={i} className="flex items-start gap-4">
                          <img
                            src={
                              false ? assets.blue_tick_icon : assets.play_icon
                            }
                            alt="play-icon"
                            className="w-4 h-4 mt-1"
                          />
                          <div
                            className="flex items-center cursor-pointer justify-between py-1 w-full text-gray-300 
                                text-xs md:text-default"
                          >
                            <p>{lecture.lectureTitle}</p>
                            <div className="flex gap-2">
                              {lecture.lectureUrl && (
                                <p className = 'text-emerald-500 cursor-pointer'
                                  onClick={() =>
                                    setPalyerData({
                                      ...lecture,
                                      chapter: index + 1,
                                      lecture: i + 1,
                                    })
                                  }
                                
                                >
                                  watch
                                </p>
                              )}
                              <p>
                                {humanizeDuration(
                                  lecture.lectureDuration * 60 * 1000,
                                  { units: ["h", "m"] }
                                )}
                              </p>
                            </div>
                          </div>
                        </li>
                      ))}
                    </ul>
                  </div>
                </div>
              ))}
          </div>

          <div className="flex items-center gap-2 py-3 mt-10">
            <h1 className="text-xl font-bold">Rate this Courses</h1>
            <Rating initialRating={0}/>
          </div>
        </div>

        {/* right column */}

        <div className="md:mt-10">
          {courseData && (
            <>
              {PalyerData ? (
                <>
                  <YouTube
                    videoId={PalyerData.lectureUrl.split("/").pop()}
                    iframeClassName="w-full aspect-video"
                  />
                  <div className="flex justify-between items-center mt-1">
                    <p>
                      {PalyerData.chapter}.{PalyerData.lecture}{" "}
                      {PalyerData.lectureTitle}
                    </p>
                    <button className="text-emerald-500">{false ? 'completed' : 'Mark Completed'}</button>
                  </div>
                </>
              ) : (
                <img src={courseData.courseThumbnail} alt="" />
              )}
            </>
          )}
        </div>
      </div>
      <Footer/>
    </>
  );
}

export default Player;
